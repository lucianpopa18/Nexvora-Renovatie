function getNestedValue(object, path) {
  return path.split(".").reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : null), object);
}

function setLanguage(lang) {
  currentLanguage = translations[lang] ? lang : defaultLanguage;
  const languagePack = translations[currentLanguage];

  document.documentElement.lang = currentLanguage;
  document.title = languagePack.meta.title;
  if (descriptionMeta) {
    descriptionMeta.setAttribute("content", languagePack.meta.description);
  }

  translatableElements.forEach((element) => {
    const key = element.dataset.i18n;
    const value = getNestedValue(languagePack, key);
    if (value !== null) {
      element.textContent = value;
    }
  });

  placeholderElements.forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    const value = getNestedValue(languagePack, key);
    if (value !== null) {
      if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
        element.placeholder = value;
      }
    }
  });

  altElements.forEach((element) => {
    const key = element.dataset.i18nAlt;
    const value = getNestedValue(languagePack, key);
    if (value !== null) {
      element.alt = value;
    }
  });

  langButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === currentLanguage);
  });

  localStorage.setItem("nexvora-language", currentLanguage);
}

function validateEmail(email) {
  return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);
}

function validatePhone(phone) {
  return /^[+()\d\s-]{8,}$/.test(phone);
}

function clearFieldErrors() {
  const fields = form.querySelectorAll("input, textarea");
  fields.forEach((field) => field.classList.remove("input-error"));
}

function showFormStatus(type, message) {
  formStatus.className = `form-status ${type}`.trim();
  formStatus.textContent = message;
}

function handleFormSubmit(event) {
  event.preventDefault();
  clearFieldErrors();
  showFormStatus("", "");

  const messages = translations[currentLanguage].formMessages;
  const name = document.getElementById("name");
  const email = document.getElementById("email");
  const phone = document.getElementById("phone");
  const message = document.getElementById("message");

  const errors = [];

  if (name.value.trim().length < 2) {
    errors.push(messages.name);
    name.classList.add("input-error");
  }

  if (!validateEmail(email.value.trim())) {
    errors.push(messages.email);
    email.classList.add("input-error");
  }

  if (!validatePhone(phone.value.trim())) {
    errors.push(messages.phone);
    phone.classList.add("input-error");
  }

  if (message.value.trim().length < 10) {
    errors.push(messages.message);
    message.classList.add("input-error");
  }

  if (errors.length > 0) {
    showFormStatus("error", errors[0]);
    return;
  }

  showFormStatus("success", messages.success);
  form.reset();
}

function handleStickyHeader() {
  header.classList.toggle("scrolled", window.scrollY > 10);
}

function setupRevealAnimations() {
  const revealElements = document.querySelectorAll(".reveal");

  if (!("IntersectionObserver" in window)) {
    revealElements.forEach((element) => element.classList.add("visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries, obs) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("visible");
          obs.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.16 }
  );

  revealElements.forEach((element) => observer.observe(element));
}

